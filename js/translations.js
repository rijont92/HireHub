let currentLanguage = localStorage.getItem('preferredLanguage') || 'en';

// Make setLanguage function globally available
window.setLanguage = function(lang) {
    if (translations[lang]) {
        currentLanguage = lang;
        localStorage.setItem('preferredLanguage', lang);
        updateTranslations();   
        // Dispatch language change event
        window.dispatchEvent(new Event('languageChanged'));
    }
}

// Make updateTranslations function globally available
window.updateTranslations = function() {
    const elements = document.querySelectorAll('[data-translate]');
    elements.forEach(element => {
        const key = element.getAttribute('data-translate');
        if (translations[currentLanguage][key]) {
            // Check if the element contains an icon
            const icon = element.querySelector('i');
            if (icon) {
                // If there's an icon, update only the text content after the icon
                const textNode = Array.from(element.childNodes).find(node => node.nodeType === Node.TEXT_NODE);
                if (textNode) {
                    textNode.textContent = translations[currentLanguage][key];
                } else {
                    // If no text node exists, create one after the icon
                    const text = document.createTextNode(translations[currentLanguage][key]);
                    element.appendChild(text);
                }
            } else {
                // If no icon, update the entire content
                element.textContent = translations[currentLanguage][key];
            }
        }
    });

    // Handle placeholders
    const placeholders = document.querySelectorAll('[data-translate-placeholder]');
    placeholders.forEach(element => {
        const key = element.getAttribute('data-translate-placeholder');
        if (translations[currentLanguage][key]) {
            element.placeholder = translations[currentLanguage][key];
        }
    });
}

// Initialize translations as early as possible
if (document.readyState === 'loading') {
    document.addEventListener('DOMContentLoaded', () => {
        updateTranslations();
    });
} else {
    // If DOM is already loaded, apply translations immediately
    updateTranslations();
}

// Listen for language changes
window.addEventListener('languageChanged', () => {
    updateTranslations();
});

// Export translations and currentLanguage
export { translations, currentLanguage }; 